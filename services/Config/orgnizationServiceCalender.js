const mm = require('../../utilities/globalModule');
const { validationResult, body } = require('express-validator');
const logger = require("../../utilities/logger");

const applicationkey = process.env.APPLICATION_KEY;

var orgnizationServiceCalender = "orgnization_service_calender";
var viewOrgnizationServiceCalender = "view_" + orgnizationServiceCalender;


function reqData(req) {

    var data = {
        ORG_ID: req.body.ORG_ID,
        IS_SERIVCE_AVAILABILE: req.body.IS_SERIVCE_AVAILABILE ? '1' : '0',
        WEEK_DAY: req.body.WEEK_DAY,
        DAY_START_TIME: req.body.DAY_START_TIME,
        DAY_END_TIME: req.body.DAY_END_TIME,
        BREAK_START_TIME: req.body.BREAK_START_TIME,
        BREAK_END_TIME: req.body.BREAK_END_TIME,
        CLIENT_ID: req.body.CLIENT_ID

    }
    return data;
}



