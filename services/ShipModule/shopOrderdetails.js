const mm = require('../../utilities/globalModule');
const { validationResult, body } = require('express-validator');
const logger = require("../../utilities/logger");

const applicationkey = process.env.APPLICATION_KEY;

var shopOrderDetails = "shop_order_details";
var viewShopOrderDetails = "view_" + shopOrderDetails;

function reqData(req) {
    var data = {
        ORDER_ID: req.body.ORDER_ID,
        BRAND_ID: req.body.BRAND_ID,
        INVENTORY_ID: req.body.INVENTORY_ID,
        CATEGORY_ID: req.body.CATEGORY_ID,
        SUB_CATEGORY_ID: req.body.SUB_CATEGORY_ID,
        QUANTITY: req.body.QUANTITY,
        RATE: req.body.RATE,
        UNIT_ID: req.body.UNIT_ID,
        UNIT_NAME: req.body.UNIT_NAME,
        TOTAL_AMOUNT: req.body.TOTAL_AMOUNT ? req.body.TOTAL_AMOUNT : 0,
        BRAND_NAME: req.body.BRAND_NAME,
        UNIQUE_NUMBER: req.body.UNIQUE_NUMBER,
        DESCRIPTION: req.body.DESCRIPTION,
        COUPON_ID: req.body.COUPON_ID,
        CLIENT_ID: req.body.CLIENT_ID
    }
    return data;
}

exports.validate = function () {
    return [
        body('ORDER_ID').isInt().optional(),
        body('BRAND_ID').isInt().optional(),
        body('INVENTORY_ID').isInt().optional(),
        body('CATEGORY_ID').isInt().optional(),
        body('SUB_CATEGORY_ID').isInt().optional(),
        body('QUANTITY').isInt().optional(),
        body('RATE').isInt().optional(),
        body('UNIT_ID').isInt().optional(),
        body('UNIT_NAME').optional(),
        body('TOTAL_AMOUNT').isDecimal().optional(),
        body('BRAND_NAME').optional(),
        body('UNIQUE_NUMBER').optional(),
        body('DESCRIPTION').optional(),
        body('COUPON_ID').isInt().optional(),
        body('ID').optional(),
    ]
}

exports.getAll = (req, res) => {
    var pageIndex = req.body.pageIndex ? req.body.pageIndex : '';
    var pageSize = req.body.pageSize ? req.body.pageSize : '';
    var start = 0;
    var end = 0;

    if (pageIndex != '' && pageSize != '') {
        start = (pageIndex - 1) * pageSize;
        end = pageSize;
    }

    let sortKey = req.body.sortKey ? req.body.sortKey : 'ID';
    let sortValue = req.body.sortValue ? req.body.sortValue : 'DESC';
    let filter = req.body.filter ? req.body.filter : '';
    var IS_FILTER_WRONG = mm.sanitizeFilter(filter);
    let criteria = '';

    if (pageIndex === '' && pageSize === '')
        criteria = filter + " order by " + sortKey + " " + sortValue;
    else
        criteria = filter + " order by " + sortKey + " " + sortValue + " LIMIT " + start + "," + end;

    let countCriteria = filter;
    var supportKey = req.headers['supportkey'];
    try {

        if (IS_FILTER_WRONG == "0") {
            mm.executeQuery('select count(*) as cnt from ' + viewShopOrderDetails + ' where 1 ' + countCriteria, supportKey, (error, results1) => {
                if (error) {
                    console.log(error);
                    logger.error(supportKey + ' ' + req.method + " " + req.url + ' ' + JSON.stringify(error), applicationkey);
                    res.status(400).json({
                        "message": "Failed to get shopOrderDetails count.",
                    });
                }
                else {
                    mm.executeQuery('select * from ' + viewShopOrderDetails + ' where 1 ' + criteria, supportKey, (error, results) => {
                        if (error) {
                            console.log(error);
                            logger.error(supportKey + ' ' + req.method + " " + req.url + ' ' + JSON.stringify(error), applicationkey);
                            res.status(400).json({
                                "message": "Failed to get shopOrderDetails information."
                            });
                        }
                        else {
                            res.status(200).json({
                                "message": "success",
                                "TAB_ID": 191,
                                "count": results1[0].cnt,
                                "data": results
                            });
                        }
                    });
                }
            });
        }
        else {
            res.status(400).json({
                message: "Invalid filter parameter.",
            });
        }
    } catch (error) {
        logger.error(supportKey + ' ' + req.method + " " + req.url + ' ' + JSON.stringify(error), applicationkey);
        console.log(error);
        res.status(500).json({
            message: "Something went wrong."
        });
    }

}

